def enhance_documents():
    """KB 문서 전체 보강"""
    with open(KB_FILE, 'r', encoding='utf-8') as f:
        data = json.load(f)

    enhanced_count = 0

    for doc in data:
        kb_id = doc.get('kb_id')

        if kb_id in ENHANCED_CONTENT:
            enhanced = ENHANCED_CONTENT[kb_id]
            doc['summary'] = enhanced['summary']
            doc['content'] = enhanced['content']
            enhanced_count += 1
            print(f"[OK] kb_id={kb_id}: {doc['title']} 보강 완료")

    # 저장
    with open(KB_FILE, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"\n총 {enhanced_count}개 문서 보강 완료")
    return enhanced_count

if __name__ == "__main__":
    enhance_documents()
